package com.gqt.utilities;

public class Bike 
{
	private String brand;
	private int milage;
	
	public Bike(String brand, int milage) 
	{
		this.brand = brand;
		this.milage = milage;
	}

	public String getBrand() 
	{
		return brand;
	}

	public int getMilage() 
	{
		return milage;
	}
	
	
	
	

}
