package com.gqt.entities;

public class Charger
{
	private String brand;
	private double voltage;
	
	public Charger(String brand, double voltage) 
	{
		this.brand = brand;
		this.voltage = voltage;
	}

	public String getBrand()
	{
		return brand;
	}

	public double getVoltage() 
	{
		return voltage;
	}
	
	
	
	

}
